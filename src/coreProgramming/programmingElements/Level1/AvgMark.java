package coreProgramming.programmingElements.Level1;// 2.	 The average percent mark in PCM

public class AvgMark {
    public static void main(String[] args) {
        byte maths = 94, chemistry = 96, physics = 95;
        double average = (maths + chemistry + physics) / 3.0;
        System.out.println("The average mark obtained in MPC is " + average);
    }
}
